#include "ft_printf.h"

int	ft_chk_typeslst(int c)
{
	return ((c == 'c') || (c == 's') || (c == 'p') || (c == 'd') || (c == 'i')
		|| (c == 'u') || (c == 'x') || (c == 'X') || (c == '%'));
}

int	ft_chk_flagslst(int c)
{
	return ((c == '-') || (c == '+') || (c == ' ') || (c == '#') || (c == '0'));
}

int	ft_chk_dotstar(int c)
{
	return ((c == '.') || (c == '*'));
}
